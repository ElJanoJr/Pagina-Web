					<div class="contenido-textos">
						<h3><span>1</span>Que es exactamente</h3>
						<p>James Webb Space Telescope (JWST)) es un observatorio espacial desarrollado
							a través de la colaboración de veinte países,construido y operado conjuntamente
							por la NASA, la Agencia Espacial Europea y la Agencia Espacial Canadiense, para sustituir
							los telescopios Hubble y Spitzer.</p>
						<h5><span>2</span>De donde viene su nombre</h5>
						<p>James Edwin Webb fue el segundo administrador de la historia de la NASA desde
							el 14 de febrero de 1961 hasta el 7 de octubre de 1968 (el primero fue T. Keith Glennan).
							Webb supervisó la NASA desde el comienzo de la administración Kennedy hasta el final
							de la administración Johnson,
							con lo que se vio inmerso como representante de las primeras misiones tripuladas
							en los Programas Mercury y Gemini
							hasta días antes del lanzamiento de la histórica primera misión Apolo.</p>
						<h3><span>3</span>Cual es su objetivo</h3>
						<p>Uno de sus principales objetivos es observar algunos de los eventos
							y objetos más distantes del universo, como la formación de las primeras galaxias.
							Este tipo de objetivos están fuera del alcance de los
							instrumentos terrestres y espaciales actuales.
							Entre sus objetivos están incluidos estudiar
							la formación de estrellas y planetas y obtener imágenes
							directas de exoplanetas y novas..</p>
						<h5><span>4</span>Su desarrollo</h5>
						<p>En desarrollo desde 1996, lo denominaron inicialmente como Next Generation
							Space Telescope o NGST, en 2002 fue denominado James E. Webb, en honor
							al funcionario del gobierno estadounidense que fue administrador de la NASA
							entre 1961 y 1968 y jugó un papel integral en el programa Apolo.El
							proyecto ha tenido numerosas demoras y gastos excesivos, siendo sometido a
							importante rediseño durante 2005. En 2011, parte del Congreso de los Estados
							Unidos optó por su cancelación, después de haber empleado en su desarrollo
							aproximadamente 3000 millones de dólares estando en producción o en fase
							de pruebas más del 75% de su hardware. En noviembre de 2011, el Congreso
							revocó los planes para cancelar el proyecto y en su lugar puso un tope de financiación
							adicional para completar el proyecto en 8000 millones de dólares. En diciembre de 2016,
							la NASA anunció que la construcción del JWST había finalizado y comenzaría su fase de
							pruebas
						</p>
